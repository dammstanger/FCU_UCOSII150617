/********************************
函数：ADNS3080_WriteRegister
功能：ADNS3080写寄存器
输入：address 寄存器地址，value 值
输出：-
修改：20141115
***********************************/
void ADNS3080_WriteRegister(uint8_t address, uint8_t value)	
{
	u8 status;	
	Clr_ADNS3080_NCS;          //使能SPI传输
#if ADNS3080_USE_SPI1	
	status =SPI1_ReadWriteByte(address|0x80);//发送寄存器号
	SPI1_ReadWriteByte(value);      //写入寄存器的值
	
#elif ADNS3080_USE_SPI2
	status =SPI2_ReadWriteByte(address|0x80);//发送寄存器号 
	SPI2_ReadWriteByte(value);      //写入寄存器的值
#endif	
	Set_ADNS3080_NCS;          //禁止SPI传输		    
}

/********************************
函数：ADNS3080_Burst_Write
功能：ADNS3080爆写
输入：address 寄存器地址，value 值	,len数据长度
输出：-
修改：20141221
***********************************/
void ADNS3080_Burst_Write(uint8_t address, uint8_t *str, uint16_t len)	
{
	u8 status;
	uint16_t i;
	Clr_ADNS3080_NCS;          //使能SPI传输
	
#if ADNS3080_USE_SPI1	
	status =SPI1_ReadWriteByte(address|0x80);//发送寄存器号 
#elif ADNS3080_USE_SPI2
	status =SPI2_ReadWriteByte(address|0x80);//发送寄存器号 
#endif		
	ADNS3080_delay_us(10);
	for(i=0;i<len;i++)
	{
#if ADNS3080_USE_SPI1	
		status =SPI1_ReadWriteByte(*str);//发送寄存器号 
#elif ADNS3080_USE_SPI2
		status =SPI2_ReadWriteByte(*str);//发送寄存器号 
#endif	
		str++;
		ADNS3080_delay_us(10);
	}
	Set_ADNS3080_NCS;          //禁止SPI传输		    
}

/********************************
函数：ADNS3080_ReadRegister
功能：ADNS3080写寄存器
输入：寄存器地址
输出：寄存器值
修改：20141115
***********************************/
u8 ADNS3080_ReadRegister(uint8_t address)
{
	u8 reg_val=1;	    
	Clr_ADNS3080_NCS;          //使能SPI传输
#if ADNS3080_USE_SPI1	
	reg_val=SPI1_ReadWriteByte(address);   //发送寄存器号
#elif ADNS3080_USE_SPI2
	reg_val=SPI2_ReadWriteByte(address);   //发送寄存器号
#endif	
	if(address==ADNS3080_MOTION)
	ADNS3080_delay_us(75);
	else
	ADNS3080_delay_us(50);
#if ADNS3080_USE_SPI1	
	reg_val=SPI1_ReadWriteByte(0X00);   //发送寄存器号
#elif ADNS3080_USE_SPI2
	reg_val=SPI2_ReadWriteByte(0X00);   //发送寄存器号
#endif	
	Set_ADNS3080_NCS;          //禁止SPI传输		    
	return(reg_val);           //返回状态值
}


/********************************
函数：ADNS3080_Get_Frame_Rate
功能：ADNS3080获取帧率
输入：-
输出：帧率值
修改：20141115
***********************************/
u16 ADNS3080_Get_Frame_Rate()
{	
	union NumericIntType period;
	uint16_t rate;
	uint32_t clockSpeed = ADNS3080_CLOCK_SPEED;
	period.byteValue[1]=ADNS3080_ReadRegister(ADNS3080_FRAME_PERIOD_UPPER); 	//读出写入的地址  
	ADNS3080_delay_us(50);
 	period.byteValue[0]=ADNS3080_ReadRegister(ADNS3080_FRAME_PERIOD_LOWER); 	
    rate = clockSpeed / period.uintValue;
    return rate;
}


