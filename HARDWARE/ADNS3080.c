/********************************
函数：ADNS3080_Update
功能：读取位移增量值
输入：-
输出：-
修改：20141115
***********************************/
// read latest values from sensor and fill in x,y and totals
void ADNS3080_Update(void)
{
    surface_quality = ADNS3080_ReadRegister(ADNS3080_SQUAL);
	ADNS3080_delay_us(50);

    // check for movement, update x,y values
    motion_reg = ADNS3080_ReadRegister(ADNS3080_MOTION);
    // check if we've had an overflow
    _overflow = ((motion_reg & 0x10) != 0);
	
    if( (motion_reg & 0x80) != 0)// &&_overflow) 
	{
        raw_dx = ((int8_t)ADNS3080_ReadRegister(ADNS3080_DELTA_X));
		ADNS3080_delay_us(50);
        raw_dy = ((int8_t)ADNS3080_ReadRegister(ADNS3080_DELTA_Y));
    }else{
        raw_dx = 0;
        raw_dy = 0;
    }
	g_A3080_dx = raw_dx;
	g_A3080_dy = raw_dy;
}


