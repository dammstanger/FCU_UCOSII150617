/********************************
函数：ADNS3080_GPIOInit
功能：初始化ADNS3080的IO口
输入：-
输出：-
修改：20141115
***********************************/
void ADNS3080_GPIOInit(void)
	{
	GPIO_InitTypeDef GPIO_InitStructure;
  //使能PORTA口时钟 
  //使能PORTB口时钟 
	RCC_APB2PeriphClockCmd(	ADNS3080_RST_CLK|ADNS3080_NCS_CLK, ENABLE );
		
	GPIO_InitStructure.GPIO_Pin = ADNS3080_NCS_PIN;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP ;   //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(ADNS3080_NCS_PORT, &GPIO_InitStructure);
		
	GPIO_InitStructure.GPIO_Pin = ADNS3080_RST_PIN;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP ;   //推挽输出GPIO_Mode_Out_OD
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(ADNS3080_RST_PORT, &GPIO_InitStructure);		
	Clr_ADNS3080_RST;		
#if ADNS3080_POWER_DOWN_CTL	
	RCC_APB2PeriphClockCmd(	ADNS3080_NPD_CLK, ENABLE );	
		
	GPIO_InitStructure.GPIO_Pin = ADNS3080_NPD_PIN;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP ;   //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(ADNS3080_NPD_PORT, &GPIO_InitStructure);
	Set_ADNS3080_NPD;									//close power down mode
#endif		
				
	}
