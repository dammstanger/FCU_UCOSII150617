/********************************
函数：ADNS3080_Print_Pixel_Data
功能：读取一帧图像
输入：-
输出：-
修改：20141115
***********************************/
// get_pixel_data - captures an image from the sensor and stores it to the
// pixe_data array
void ADNS3080_Print_Pixel_Data()
{
    int16_t i,j;
//    u8 firstpixel = 1;
    uint8_t regValue;
    uint8_t pixelValue;
	uint8_t pixeldata[10] = {0};
	static u8 cnt = 0;

    // write to frame capture register to force capture of frame
    ADNS3080_WriteRegister(ADNS3080_FRAME_CAPTURE,0x83);

    // wait 3 frame periods + 10 nanoseconds for frame to be captured
    // min frame speed is 2000 frames/second so 1 frame = 500 nano seconds.
    // so 500 x 3 + 10 = 1510
	ADNS3080_delay_us(1510);

    // display the pixel data
    for( i=0; i<ADNS3080_PIXELS_Y; i++ ) {
        for( j=0; j<ADNS3080_PIXELS_X; j++ ) {
            regValue = ADNS3080_ReadRegister(ADNS3080_FRAME_CAPTURE);
            pixelValue = ( regValue << 2 );
			pixeldata[cnt++]=pixelValue;
			if(cnt==10)
			{
				cnt = 0;
				NRF24L01_TxPacket(pixeldata,10);
			}
//            printf("%3d",pixelValue);
			ADNS3080_delay_us(50);
        }
    }

    // hardware reset to restore sensor to normal operation
    ADNS3080_Reset();
}



